package edunova;

public class K01_While {
	
	public static void main(String[] args) {
		
		
		//beskonaèna while petlja
		while(true) {
			System.out.println("Osijek");
			break;
		}
		
		
		int i = 10;
		while(i>0){
			System.out.println(i--);
		}
		
		int j=3;i=1;
		
		while(j==3 & i<10) {
			System.out.println(i++);
		}
		
	}

}
